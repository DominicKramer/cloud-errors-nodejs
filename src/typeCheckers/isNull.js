/**
 * Checks the input for type. If input is of type null then the function will
 * return true, otherwise the function will return false.
 * @function isNull
 * @param {Any} varToCheck - the variable to check the type of
 * @returns {Boolean} - Returns true if input is of type null, otherwise false
 */
function isNull ( varToCheck ) {

  return varToCheck === null;
}
